# ----------------------------------------------------------
# 1. Create vectors with c()
# ----------------------------------------------------------

numbers <- c(10, 20, 30, 40, 50)
fruits <- c("apple", "banana", "orange")
flags <- c(TRUE, FALSE, TRUE)

numbers
fruits
flags


# ----------------------------------------------------------
# 2. Check vector type
# ----------------------------------------------------------

class(numbers)
class(fruits)
class(flags)


# ----------------------------------------------------------
# 3. Length of a vector
# ----------------------------------------------------------

length(numbers)
length(fruits)
length(flags)


# ----------------------------------------------------------
# 4. Basic calculations with numeric vectors
# ----------------------------------------------------------

sum(numbers)
mean(numbers)
max(numbers)
min(numbers)


# ----------------------------------------------------------
# 5. Vectorized operations
# ----------------------------------------------------------

numbers + 5
numbers * 2
numbers / 10


# ----------------------------------------------------------
# 6. Access elements by index
# ----------------------------------------------------------

numbers[1]
numbers[3]
fruits[2]


# ----------------------------------------------------------
# 7. Extract multiple elements
# ----------------------------------------------------------

numbers[c(1, 3, 5)]
fruits[c(1, 2)]


# ----------------------------------------------------------
# 8. Exclude elements
# ----------------------------------------------------------

numbers[-1]
numbers[-c(2, 4)]


# ----------------------------------------------------------
# 9. Create a sequence
# ----------------------------------------------------------

1:10
seq(0, 20, by = 5)


# ----------------------------------------------------------
# 10. Repeat values
# ----------------------------------------------------------

rep(1, 5)
rep(c("A", "B"), 3)


# ----------------------------------------------------------
# 11. Logical filtering
# ----------------------------------------------------------

numbers > 25
numbers[numbers > 25]


# ----------------------------------------------------------
# 12. Combine vectors
# ----------------------------------------------------------

more_numbers <- c(60, 70)
all_numbers <- c(numbers, more_numbers)

all_numbers


# ----------------------------------------------------------
# End of script
# ----------------------------------------------------------

