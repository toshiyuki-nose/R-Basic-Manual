# ----------------------------------------------------------
# 1. Prepare sample data
# ----------------------------------------------------------

scores <- c(85, 90, 78, 92, 88, 76, 95, 89, 84, 91)

scores


# ----------------------------------------------------------
# 2. Basic summary values
# ----------------------------------------------------------

sum(scores)
mean(scores)
median(scores)
min(scores)
max(scores)
range(scores)


# ----------------------------------------------------------
# 3. Measure of spread
# ----------------------------------------------------------

var(scores)
sd(scores)


# ----------------------------------------------------------
# 4. Quantiles
# ----------------------------------------------------------

quantile(scores)
quantile(scores, probs = c(0.25, 0.5, 0.75))


# ----------------------------------------------------------
# 5. Sort values
# ----------------------------------------------------------

sort(scores)
sort(scores, decreasing = TRUE)


# ----------------------------------------------------------
# 6. Count values
# ----------------------------------------------------------

length(scores)


# ----------------------------------------------------------
# 7. Summary function
# ----------------------------------------------------------

summary(scores)


# ----------------------------------------------------------
# 8. Basic statistics on a data frame column
# ----------------------------------------------------------

students <- data.frame(
  id = c(1, 2, 3, 4, 5),
  name = c("Alice", "Bob", "Carol", "David", "Emma"),
  score = c(85, 90, 78, 92, 88)
)

students

mean(students$score)
summary(students$score)


# ----------------------------------------------------------
# 9. Check conditions
# ----------------------------------------------------------

scores > 85
scores[scores > 85]


# ----------------------------------------------------------
# 10. Missing values
# ----------------------------------------------------------

scores_with_na <- c(85, 90, NA, 92, 88)

scores_with_na
mean(scores_with_na)
mean(scores_with_na, na.rm = TRUE)
summary(scores_with_na)


