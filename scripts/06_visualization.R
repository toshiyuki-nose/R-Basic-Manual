# ----------------------------------------------------------
# 1. Prepare sample data
# ----------------------------------------------------------

scores <- c(85, 90, 78, 92, 88, 76, 95, 89, 84, 91)


# ----------------------------------------------------------
# 2. Basic scatter plot
# ----------------------------------------------------------

x <- 1:length(scores)

plot(x, scores)


# ----------------------------------------------------------
# 3. Scatter plot with labels
# ----------------------------------------------------------

plot(
  x, scores,
  main = "Score Distribution",
  xlab = "Index",
  ylab = "Score",
  pch = 19
)


# ----------------------------------------------------------
# 4. Line plot
# ----------------------------------------------------------

plot(
  x, scores,
  type = "l",
  main = "Score Trend",
  xlab = "Index",
  ylab = "Score"
)


# ----------------------------------------------------------
# 5. Histogram
# ----------------------------------------------------------

hist(
  scores,
  main = "Histogram of Scores",
  xlab = "Score",
  col = "lightblue"
)


# ----------------------------------------------------------
# 6. Boxplot
# ----------------------------------------------------------

boxplot(
  scores,
  main = "Boxplot of Scores",
  col = "lightgreen"
)


# ----------------------------------------------------------
# 7. Barplot
# ----------------------------------------------------------

counts <- table(c("A", "B", "A", "C", "B", "A"))

barplot(
  counts,
  main = "Category Count",
  col = "orange"
)


# ----------------------------------------------------------
# 8. Plot from data frame
# ----------------------------------------------------------

students <- data.frame(
  id = 1:5,
  score = c(85, 90, 78, 92, 88)
)

plot(
  students$id,
  students$score,
  main = "Student Scores",
  xlab = "ID",
  ylab = "Score",
  pch = 19
)


# ----------------------------------------------------------
# End of script
# ----------------------------------------------------------

