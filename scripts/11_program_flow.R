# ----------------------------------------------------------
# 1. Prepare sample data
# ----------------------------------------------------------

scores <- c(85, 90, 78, 92, 88)

scores


# ----------------------------------------------------------
# 2. if statement
# ----------------------------------------------------------

score <- 85

if (score >= 80) {
  print("Pass")
}


# ----------------------------------------------------------
# 3. if ... else
# ----------------------------------------------------------

score <- 75

if (score >= 80) {
  print("Pass")
} else {
  print("Fail")
}


# ----------------------------------------------------------
# 4. ifelse()
# ----------------------------------------------------------

ifelse(scores >= 80, "Pass", "Fail")


# ----------------------------------------------------------
# 5. for loop
# ----------------------------------------------------------

for (score in scores) {
  print(score)
}


# ----------------------------------------------------------
# 6. while loop
# ----------------------------------------------------------

count <- 1

while (count <= 5) {
  print(count)
  count <- count + 1
}


# ----------------------------------------------------------
# 7. break
# ----------------------------------------------------------

for (score in scores) {
  
  if (score == 92) {
    break
  }
  
  print(score)
  
}


# ----------------------------------------------------------
# 8. next
# ----------------------------------------------------------

for (score in scores) {
  
  if (score < 80) {
    next
  }
  
  print(score)
  
}


# ----------------------------------------------------------
# 9. Apply program flow to data analysis
# ----------------------------------------------------------

high_scores <- c()

for (score in scores) {
  
  if (score >= 90) {
    high_scores <- c(high_scores, score)
  }
  
}

high_scores


# ----------------------------------------------------------
# 10. Compare with the average
# ----------------------------------------------------------

average_score <- mean(scores)

above_average <- c()

for (score in scores) {
  
  if (score > average_score) {
    above_average <- c(above_average, score)
  }
  
}

average_score
above_average


# ----------------------------------------------------------
# End of script
# ----------------------------------------------------------

